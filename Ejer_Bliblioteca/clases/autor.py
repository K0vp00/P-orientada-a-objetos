class Autor: #clase
    def __init__(self, id_autor, a_nombre, a_apellido, biografia, nacionalidad, fech_nacimiento):
        self.id_autor = id_autor #atributo de la clase
        self.a_nombre = a_nombre
        self.a_apellido = a_apellido
        self.biografia = biografia
        self.nacionalidad = nacionalidad
        self.fech_nacimiento = fech_nacimiento